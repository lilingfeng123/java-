package entity;

public class Car {
	private int c_s_id;
	private String c_a_id;
	private int c_id;
	private String c_name;
	private double c_price;
	private int c_num;
	private double c_weight;
	private String c_type;
	public int getC_s_id() {
		return c_s_id;
	}
	public void setC_s_id(int c_s_id) {
		this.c_s_id = c_s_id;
	}
	public String getC_a_id() {
		return c_a_id;
	}
	public void setC_a_id(String c_a_id) {
		this.c_a_id = c_a_id;
	}
	public int getC_id() {
		return c_id;
	}
	public void setC_id(int c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public double getC_price() {
		return c_price;
	}
	public void setC_price(double c_price) {
		this.c_price = c_price;
	}
	public int getC_num() {
		return c_num;
	}
	public void setC_num(int c_num) {
		this.c_num = c_num;
	}
	public double getC_weight() {
		return c_weight;
	}
	public void setC_weight(double c_weight) {
		this.c_weight = c_weight;
	}
	public String getC_type() {
		return c_type;
	}
	public void setC_type(String c_type) {
		this.c_type = c_type;
	}
	
}
